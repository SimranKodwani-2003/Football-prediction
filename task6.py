import pandas as pd
from flask import Flask, render_template, request
from sklearn.model_selection import train_test_split
from sklearn.svm import SVR

app = Flask(__name__)

# Load data
df = pd.read_csv("/content/Predicting the Price of a Football Player.csv")
df.drop(["name", "club", "position", "nationality", "fpl_sel"], axis=1, inplace=True)

# Prepare training data
x = df.drop(columns=["market_value"]).to_numpy()
y = df["market_value"].to_numpy()
x_train, x_test, y_train, y_test = train_test_split(x, y, test_size=0.25)

# Train model once before starting the app
model = SVR()
model.fit(x_train, y_train)

@app.route("/", methods=['GET', 'POST'])
def index():
    return render_template("ml_assignment.html")

@app.route("/data", methods=['GET', 'POST'])
def data():
    if request.method == "POST":
        try:
            # Convert input values to integers
            li = [
                int(request.form['age']),
                int(request.form['position_cat']),
                int(request.form['page_views']),
                int(request.form['fpl_value']),
                int(request.form['fpl_points']),
                int(request.form['region']),
                int(request.form['new_foreign']),
                int(request.form['age_cat']),
                int(request.form['club_id']),
                int(request.form['big_club']),
                int(request.form['new_signing'])
            ]
            market_value = model.predict([li])[0]  # Extract the first value for rendering
        except Exception as e:
            return f"Error: {e}"

    return render_template("ml_data.html", market_value=market_value)

if __name__ == "__main__":
    app.run(debug=True)